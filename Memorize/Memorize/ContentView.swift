                base.fill(.white)
                base.strokeBorder(lineWidth: 2)
                Text("ðŸ‘»").font(.largeTitle)
            } else {
                base.fill()
            }
        }
        .onTapGesture {
            isFaceUp.toggle()
        }
    }
}







